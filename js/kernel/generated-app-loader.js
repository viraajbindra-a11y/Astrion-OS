// Astrion OS — Generated App Loader (M4 dock surface)
//
// M4.P4's promoteApp flips a 'generated-app' graph node from
// status='sandboxed' to status='docked'. This loader is the missing
// piece that the renderer needs: read those docked nodes, register
// each one with processManager, and emit a 'dock:reload' so the dock
// UI re-renders and shows the new icons.
//
// Launch behavior (MVP): the click opens an "info card" window
// showing the spec goal, acceptance criteria, model+brain per
// generation phase, attempt count, and the verified code blob. We
// deliberately do NOT execute the code yet — running an LLM-generated
// blob in the host page is a separate safety conversation. The M4
// chain proves "code passes its own tests in a sandbox"; running it
// in production needs an iframe wrapper + UI scaffold (M4.P5).
//
// Why this lives in /kernel/ not /shell/: it bridges graph state
// (kernel) to processManager (also kernel). The dock-side changes
// are tiny — a single import + appended list — and live in dock.js.

import { eventBus } from './event-bus.js';
import { processManager } from './process-manager.js';
import { listGeneratedApps } from './app-promoter.js';

const REGISTERED = new Set();

function shortTitle(app) {
  const raw = String(app.intent || app.id || '').trim();
  if (raw.length <= 20) return raw || 'Generated';
  return raw.slice(0, 18) + '…';
}

function infoCardHtml(app) {
  const goal = app.intent || '(no intent recorded)';
  const phases = app.provenance || {};
  const fmt = (n) => typeof n === 'number' ? new Date(n).toLocaleString() : '—';
  return `
    <div style="padding:16px 20px;font-family:-apple-system,BlinkMacSystemFont,sans-serif;color:#e0e0e0;background:#1a1a1f;height:100%;overflow:auto;">
      <div style="font-size:11px;text-transform:uppercase;letter-spacing:0.5px;color:#888;margin-bottom:6px;">Generated app · ${app.status}</div>
      <h1 style="font-size:20px;font-weight:600;margin:0 0 16px 0;line-height:1.3;">${escapeHtml(goal)}</h1>

      <div style="background:#23232a;border-radius:8px;padding:12px 14px;margin-bottom:12px;">
        <div style="font-size:11px;color:#888;margin-bottom:6px;">Provenance</div>
        <div style="display:grid;grid-template-columns:auto 1fr;gap:6px 12px;font-size:12px;">
          <span style="color:#888;">spec model</span><span>${escapeHtml(phases.specModel || '—')} (${escapeHtml(phases.specBrain || '?')})</span>
          <span style="color:#888;">tests model</span><span>${escapeHtml(phases.suiteModel || '—')}</span>
          <span style="color:#888;">code model</span><span>${escapeHtml(phases.codeModel || '—')} (${escapeHtml(phases.codeBrain || '?')})</span>
          <span style="color:#888;">attempts</span><span>${phases.codeAttempts ?? '—'}</span>
          <span style="color:#888;">tests passed</span><span>${phases.testsPassed ?? '—'}/${phases.testsTotal ?? '—'}</span>
          <span style="color:#888;">created</span><span>${fmt(app.createdAt)}</span>
          <span style="color:#888;">docked</span><span>${fmt(app.dockedAt)}</span>
        </div>
      </div>

      <div style="background:#23232a;border-radius:8px;padding:12px 14px;margin-bottom:12px;">
        <div style="font-size:11px;color:#888;margin-bottom:6px;">Graph IDs</div>
        <div style="font-family:ui-monospace,monospace;font-size:11px;line-height:1.5;color:#aaa;">
          app: ${escapeHtml(app.id)}<br>
          spec: ${escapeHtml(app.specId || '—')}<br>
          suite: ${escapeHtml(app.suiteId || '—')}<br>
          code: ${escapeHtml(app.codeId || '—')}
        </div>
      </div>

      <div style="font-size:11px;color:#888;line-height:1.5;">
        Code execution from this card is deferred to M4.P5 — sandboxed
        iframe runner with a UI scaffold. The code itself is verified
        by the M4.P3 sandbox suite at generation time; running it in
        the host page is a separate safety review.
      </div>
    </div>
  `;
}

function escapeHtml(s) {
  return String(s).replace(/[&<>"']/g, (c) => ({
    '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;'
  }[c]));
}

/**
 * Read every docked 'generated-app' node and register each with
 * processManager so dock + spotlight + launchpad can launch them.
 * Idempotent — re-registering an existing id just updates the
 * definition. Returns the list of `{ id, name }` for the dock UI.
 */
export async function loadGeneratedApps() {
  let apps;
  try {
    apps = await listGeneratedApps('docked');
  } catch (err) {
    console.warn('[generated-app-loader] listGeneratedApps failed:', err?.message);
    return [];
  }
  const dockEntries = [];
  for (const app of apps) {
    const id = app.id;
    const name = shortTitle(app);
    processManager.register(id, {
      name,
      icon: '\u2728', // ✨ — generated by AI
      iconClass: 'dock-icon-generated',
      singleInstance: true,
      width: 520,
      height: 480,
      minWidth: 380,
      minHeight: 320,
      launch: (contentEl) => { contentEl.innerHTML = infoCardHtml(app); },
    });
    REGISTERED.add(id);
    dockEntries.push({ id, name });
  }
  return dockEntries;
}

/**
 * Wire up the loader to react to app:promoted events. The dock can
 * subscribe to 'dock:reload' (we re-emit after every reload) to
 * re-render its icon list.
 */
export function initGeneratedAppLoader() {
  const reload = async () => {
    const entries = await loadGeneratedApps();
    eventBus.emit('dock:reload', { generatedApps: entries });
  };
  eventBus.on('app:promoted', reload);
  eventBus.on('app:archived', reload);
  // Initial load
  reload();
}

// ─── Sanity tests ───

if (typeof window !== 'undefined' && window.location?.hostname === 'localhost') {
  // shortTitle behavior
  const checks = [
    [shortTitle({ intent: 'short' }), 'short'],
    [shortTitle({ intent: 'a much longer intent that should be truncated nicely' }), 'a much longer inte\u2026'],
    [shortTitle({}), 'Generated'],
  ];
  let f = 0;
  for (const [got, want] of checks) {
    if (got !== want) { console.warn('[generated-app-loader] shortTitle FAIL:', { got, want }); f++; }
  }
  if (f === 0) console.log('[generated-app-loader] all 3 sanity tests pass');
}
